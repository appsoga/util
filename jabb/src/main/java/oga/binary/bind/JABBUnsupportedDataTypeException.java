package oga.binary.bind;

public class JABBUnsupportedDataTypeException extends JABBException {

	private static final long serialVersionUID = -704732627531337094L;

	public JABBUnsupportedDataTypeException() {
		super();
	}

	public JABBUnsupportedDataTypeException(String message, Throwable cause) {
		super(message, cause);
	}

	public JABBUnsupportedDataTypeException(String message) {
		super(message);
	}

	public JABBUnsupportedDataTypeException(Throwable cause) {
		super(cause);
	}

}
