package oga.binary.bind;

public class JABBMarshallException extends JABBException {

	private static final long serialVersionUID = -4735394690508669600L;

	public JABBMarshallException() {
		super();
	}

	public JABBMarshallException(String message, Throwable cause) {
		super(message, cause);
	}

	public JABBMarshallException(String message) {
		super(message);
	}

	public JABBMarshallException(Throwable cause) {
		super(cause);
	}

}
