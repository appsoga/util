package oga.binary.bind;

public class JABBUnmarshallException extends JABBException {

	private static final long serialVersionUID = -3021859674974595320L;

	public JABBUnmarshallException() {
		super();
	}

	public JABBUnmarshallException(String message, Throwable cause) {
		super(message, cause);
	}

	public JABBUnmarshallException(String message) {
		super(message);
	}

	public JABBUnmarshallException(Throwable cause) {
		super(cause);
	}

}
