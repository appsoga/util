package oga.binary.bind;

public class JABBException extends Exception {

	private static final long serialVersionUID = -3527375191823388989L;

	public JABBException() {
		super();
	}

	public JABBException(String message, Throwable cause) {
		super(message, cause);
	}

	public JABBException(String message) {
		super(message);
	}

	public JABBException(Throwable cause) {
		super(cause);
	}

}
